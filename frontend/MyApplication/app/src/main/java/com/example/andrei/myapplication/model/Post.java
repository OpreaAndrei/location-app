package com.example.andrei.myapplication.model;

public class Post {
    private String firstName;
    private String lastName;
    private String email;
    private int id;

    public String getFirstName() {
        return firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public String getEmail() {
        return email;
    }

    public int getId() {
        return id;
    }
}
